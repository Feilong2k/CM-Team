// hello.js - Hello World in JavaScript
console.log("Hello World!");

// You can also create a function that returns hello world
function sayHello() {
    return "Hello World!";
}

// Call the function
const message = sayHello();
console.log(message);

// Or use an arrow function
const helloArrow = () => "Hello World from arrow function!";
console.log(helloArrow());